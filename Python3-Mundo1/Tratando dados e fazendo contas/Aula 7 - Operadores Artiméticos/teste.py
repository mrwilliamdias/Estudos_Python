a = int(input('Digite o valor de A: '))
b = int(input('Digite o valor de B: '))
print('{} + {} = {}'.format(a, b, a + b))
print('{} - {} = {}'.format(a, b, a - b))
print('{} * {} = {}'.format(a, b, a * b))
print('{} / {} = {:.2f}'.format(a, b, a / b))
print('{} // {} = {}'.format(a, b, a // b))
print('{} ** {} = {}'.format(a, b, pow(a, b)))
print('{} ** (1/2) = {:.2f}'.format(a, a ** (1/2)))
print('{} % {} = {:.2f}'.format(a, b, a % b))
